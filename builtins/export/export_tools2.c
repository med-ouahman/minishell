unsigned int	key_size(char *s1)
{
	unsigned int	l1;

	l1 = 0;
	while (s1[l1] && s1[l1] != '=')
		l1++;
	return (l1);
}

void	swap_ptrs(char **p1, char **p2)
{
	char	*tmp;

	tmp = *p1;
	*p1 = *p2;
	*p2 = tmp;
}
